
public class ControleDeFluxo {
    
    public static void main(String[] args) {
        
        int num = 11;
        
        if (num > 10){
            System.out.println("Num maior que 10");
        } else{
            if (num < 10 ){
                System.out.println("Num é menor que 10");                                       
            } else {
                System.out.println("Num é igual a 10");
            }
        }
    }
    
}
