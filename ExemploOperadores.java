
public class ExemploOperadores {
    
    public static void main(String[] args) {
        
        int x,y,z;
        
        x=6;
        y=3;
        
        z = x+y;
        System.out.println("Soma é "+z);
        
        z = x-y;
        System.out.println("Subtração é "+z);
        
        z = x * y;
        System.out.println("Multiplicação é "+z);
        
        z = x / y;
        System.out.println("Divisão é "+z);
        
        z = x % y;
        System.out.println("Módulo é "+z);
        
        
        
        
    }
    
}
