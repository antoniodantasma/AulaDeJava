
public class TesteSwitchCase {
    
    public static void main(String[] args) {
        
        int num = 20;
        
        switch (num){
            case 10:
                System.out.println("O num é 10");
            break;
            
            case 5:
                System.out.println("O num é 5");
            break;
            
            case 0:
                System.out.println("O num é 0");
            break;
            default:
                System.out.println("O Num é inválido");
        }
                
    }
    
}
